function showNextModal(results) {
    var modalBody = "<h5 class=\"font-bold text-lg mb-3\">Results generated by the compatibility test</h5><div class=\"mt-3\"><pre id=\"result-content\" class=\"language-yaml overflow-x-auto p-4 rounded-lg bg-gray-100 dark:bg-gray-700\">" + (results || "").replace(/</g, "&lt;") + "</pre></div>";
    var modalFooter = "<button type=\"button\" class=\"px-4 py-2 rounded-xl bg-red-600 text-white font-semibold\" onclick=\"reloadPage();\">Cancel</button><button type=\"button\" class=\"px-4 py-2 rounded-xl medperf-bg dark:bg-green-600 text-white font-semibold close-modal-btn\" onclick=\"showRegisterContainer();\">Continue to container registration</button>";
    var extra = function () {
        var el = document.getElementById("result-content");
        if (window.Prism && el) Prism.highlightElement(el);
    };
    showModal({ title: "Model Compatibility Test Successful", body: modalBody, footer: modalFooter, modalClasses: "max-w-4xl w-full", extra_func: extra });
}

function onContainerCompatibilityTestSuccess(response) {
    markAllStagesAsComplete();
    if (response && response.status === "success") {
        var results = JSON.stringify(response.results || {}, null, 2);
        showNextModal(results);
    } else {
        showErrorModal("Model Compatibility Test Failed", response);
    }
}

function runContainerCompatibilityTest(runCompTestButton) {
    addSpinner(runCompTestButton);
    var form = document.getElementById("container-comp-test-form");
    var formData = form ? new FormData(form) : new FormData();
    disableElements("#container-comp-test-form input, #container-comp-test-form select, #container-comp-test-form button");
    ajaxRequest("/containers/run_compatibility_test", "POST", formData, onContainerCompatibilityTestSuccess, "Error running container compatibility test:");
    showPanel("Running Compatibility Test...");
    getTaskId().then(function (id) { window.runningTaskId = id; });
    if (typeof streamEvents === "function") streamEvents(logPanel, stagesList, currentStageElement);
}

function checkCompTestFormValidity() {
    var containerFileEl = document.getElementById("container-file");
    var containerPath = containerFileEl ? containerFileEl.value.trim() : "";
    var benchmarkEl = document.getElementById("benchmark");
    var checked = document.querySelector("input[name='model_encrypted']:checked");
    var isEncrypted = checked ? checked.value : "";
    var decryptionEl = document.getElementById("decryption-file");
    var decryptionPath = decryptionEl ? decryptionEl.value.trim() : "";
    var isValid = !!(benchmarkEl && benchmarkEl.value) && containerPath.length > 0 && containerPath.endsWith(".yaml") && (isEncrypted === "true" ? decryptionPath.length > 0 : isEncrypted === "false");
    var runBtn = document.getElementById("run-comp-test-btn");
    if (runBtn) runBtn.disabled = !isValid;
}

function showRegisterContainer() {
    window.location.href = "/containers/register/ui";
}

function init() {
    var runBtn = document.getElementById("run-comp-test-btn");
    if (runBtn) runBtn.addEventListener("click", function (e) { showConfirmModal(e.currentTarget, runContainerCompatibilityTest, "run the compatibility test?"); });
    var form = document.getElementById("container-comp-test-form");
    if (form) form.querySelectorAll("input, select").forEach(function (el) {
        el.addEventListener("change", checkCompTestFormValidity);
        el.addEventListener("keyup", checkCompTestFormValidity);
    });
    var browseDec = document.getElementById("browse-decryption-btn");
    if (browseDec) browseDec.addEventListener("click", function () { browseWithFiles = true; browseFolderHandler("decryption-file"); });
    document.querySelectorAll("input[name='model_encrypted']").forEach(function (radio) {
        radio.addEventListener("change", function () {
            var withEnc = document.getElementById("with-encryption");
            var decContainer = document.getElementById("decryption-file-container");
            var decFile = document.getElementById("decryption-file");
            if (withEnc && withEnc.checked) {
                if (decContainer) { decContainer.style.display = ""; decContainer.classList.remove("hidden"); }
            } else {
                if (decContainer) { decContainer.style.display = "none"; decContainer.classList.add("hidden"); }
                if (decFile) decFile.value = "";
            }
        });
    });
    var browseContainer = document.getElementById("browse-container-btn");
    if (browseContainer) browseContainer.addEventListener("click", function () { browseWithFiles = true; browseFolderHandler("container-file"); });
    var browseParams = document.getElementById("browse-parameters-btn");
    if (browseParams) browseParams.addEventListener("click", function () { browseWithFiles = true; browseFolderHandler("parameters-file"); });
    var browseAdditional = document.getElementById("browse-additional-btn");
    if (browseAdditional) browseAdditional.addEventListener("click", function () { browseWithFiles = false; browseFolderHandler("additional-file"); });
    checkCompTestFormValidity();
}
if (document.readyState === "loading") document.addEventListener("DOMContentLoaded", init);
else init();
