import os
from medperf.commands.association.utils import get_user_associations
import yaml
from typing import List

from medperf.utils import get_folders_hash, remove_path
from medperf.entities.interface import Entity
from medperf.entities.schemas import DatasetSchema
import medperf.config as config
from medperf.account_management import get_medperf_user_data
from medperf.entities.utils import handle_validation_error
from medperf.exceptions import InvalidEntityError
import logging


class Dataset(Entity):
    """
    Class representing a Dataset

    Datasets are stored locally in the Data Owner's machine. They contain
    information regarding the prepared dataset, such as name and description,
    general statistics and an UID generated by hashing the contents of the
    data preparation output.
    """

    @staticmethod
    def get_type():
        return "dataset"

    @staticmethod
    def get_storage_path():
        return config.datasets_folder

    @staticmethod
    def get_comms_retriever():
        return config.comms.get_dataset

    @staticmethod
    def get_metadata_filename():
        return config.reg_file

    @staticmethod
    def get_comms_uploader():
        return config.comms.upload_dataset

    @handle_validation_error
    def __init__(self, **kwargs):
        self._model = DatasetSchema(**kwargs)
        super().__init__()
        self.state = self._model.state
        self.description = self._model.description
        self.location = self._model.location
        self.input_data_hash = self._model.input_data_hash
        self.generated_uid = self._model.generated_uid
        self.data_preparation_mlcube = self._model.data_preparation_mlcube
        self.split_seed = self._model.split_seed
        self.generated_metadata = self._model.generated_metadata
        self.user_metadata = self._model.user_metadata
        self.report = self._model.report
        self.submitted_as_prepared = self._model.submitted_as_prepared

        self._set_helper_attributes()

    def _set_helper_attributes(self):
        self.data_path = os.path.join(self.path, "data")
        self.labels_path = os.path.join(self.path, "labels")
        self.report_path = os.path.join(self.path, config.report_file)
        self.metadata_path = os.path.join(self.path, config.metadata_folder)
        self.statistics_path = os.path.join(self.path, config.statistics_filename)

    @property
    def local_id(self):
        return self.generated_uid

    def get_cc_config(self):
        cc_values = self.user_metadata.get("cc", {})
        return cc_values.get("config", {})

    def set_cc_config(self, cc_config: dict):
        if "cc" not in self.user_metadata:
            self.user_metadata["cc"] = {}
        self.user_metadata["cc"]["config"] = cc_config

    def get_cc_policy(self):
        cc_values = self.user_metadata.get("cc", {})
        return cc_values.get("policy", {})

    def set_cc_policy(self, cc_policy: dict):
        if "cc" not in self.user_metadata:
            self.user_metadata["cc"] = {}
        self.user_metadata["cc"]["policy"] = cc_policy

    def is_cc_configured(self):
        return self.get_cc_config() != {}

    def is_operational(self):
        return self.state == "OPERATION"

    def set_raw_paths(self, raw_data_path: str, raw_labels_path: str):
        raw_paths_file = os.path.join(self.path, config.dataset_raw_paths_file)
        data = {"data_path": raw_data_path, "labels_path": raw_labels_path}
        with open(raw_paths_file, "w") as f:
            yaml.dump(data, f)

    def get_raw_paths(self):
        raw_paths_file = os.path.join(self.path, config.dataset_raw_paths_file)
        with open(raw_paths_file) as f:
            data = yaml.safe_load(f)
        return data["data_path"], data["labels_path"]

    def mark_as_ready(self):
        flag_file = os.path.join(self.path, config.ready_flag_file)
        with open(flag_file, "w"):
            pass

    def unmark_as_ready(self):
        flag_file = os.path.join(self.path, config.ready_flag_file)
        remove_path(flag_file)

    def is_ready(self):
        flag_file = os.path.join(self.path, config.ready_flag_file)
        return os.path.exists(flag_file)

    def calculate_raw_hash(self):
        raw_data_path, raw_labels_path = self.get_raw_paths()
        calculated_hash = get_folders_hash([raw_data_path, raw_labels_path])
        logging.debug(f"Raw dataset calculated hash: {calculated_hash}")
        return calculated_hash

    def calculate_prepared_hash(self):
        calculated_hash = get_folders_hash([self.data_path, self.labels_path])
        logging.debug(f"Prepared dataset calculated hash: {calculated_hash}")
        return calculated_hash

    def check_hash(self):
        if not self.is_operational():
            raise InvalidEntityError("Dataset is not operational. Cannot check hash.")
        calculated_hash = self.calculate_prepared_hash()
        return calculated_hash == self.generated_uid

    @staticmethod
    def remote_prefilter(filters: dict) -> callable:
        """Applies filtering logic that must be done before retrieving remote entities

        Args:
            filters (dict): filters to apply

        Returns:
            callable: A function for retrieving remote entities with the applied prefilters
        """
        comms_fn = config.comms.get_datasets
        if "owner" in filters and filters["owner"] == get_medperf_user_data()["id"]:
            comms_fn = config.comms.get_user_datasets

        if "mlcube" in filters and filters["mlcube"] is not None:

            def func():
                return config.comms.get_mlcube_datasets(filters["mlcube"])

            comms_fn = func

        return comms_fn

    @classmethod
    def get_benchmarks_associations(cls, dataset_uid: int) -> List[dict]:
        """Retrieves the list of benchmarks dataset is associated with

        Args:
            dataset_uid (int): UID of the dataset.

        Returns:
            List[dict]: List of associations
        """

        experiment_type = "benchmark"
        component_type = "dataset"

        associations = get_user_associations(
            experiment_type=experiment_type,
            component_type=component_type,
            approval_status=None,
        )

        associations = [a for a in associations if a["dataset"] == dataset_uid]

        return associations

    def read_report(self):
        """Reads the report if it exists"""
        if os.path.exists(self.report_path):
            with open(self.report_path, "r") as f:
                self.report = yaml.safe_load(f.read())
        return self.report

    def read_statistics(self):
        """Reads the report if it exists"""
        if os.path.exists(self.statistics_path):
            with open(self.statistics_path, "r") as f:
                self.generated_metadata = yaml.safe_load(f.read())
        return self.generated_metadata

    def display_dict(self):
        return {
            "UID": self.identifier,
            "Name": self.name,
            "Description": self.description,
            "Location": self.location,
            "Data Preparation Container UID": self.data_preparation_mlcube,
            "Generated Hash": self.generated_uid,
            "State": self.state,
            "Created At": self.created_at,
            "Registered": self.is_registered,
            "Submitted as Prepared": self.submitted_as_prepared,
            "Status": "\n".join([f"{k}: {v}" for k, v in self.report.items()]),
            "Owner": self.owner,
        }
