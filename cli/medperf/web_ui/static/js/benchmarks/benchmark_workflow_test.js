function showNextModal(results) {
    var modalBody = "<h5 class=\"font-bold text-lg mb-3\">Results generated by the workflow test</h5><div class=\"mt-3\"><pre id=\"result-content\" class=\"language-yaml overflow-x-auto p-4 rounded-lg bg-gray-100 dark:bg-gray-700\">" + (results || "").replace(/</g, "&lt;") + "</pre></div>";
    var modalFooter = "<button type=\"button\" class=\"px-4 py-2 rounded-xl bg-red-600 text-white font-semibold\" onclick=\"reloadPage();\">Cancel</button><button type=\"button\" class=\"px-4 py-2 rounded-xl medperf-bg dark:bg-green-600 text-white font-semibold close-modal-btn\" onclick=\"showRegisterBenchmark();\">Continue to benchmark registration</button>";
    var extra = function () {
        var el = document.getElementById("result-content");
        if (window.Prism && el) Prism.highlightElement(el);
    };
    showModal({ title: "Benchmark Workflow Test Successful", body: modalBody, footer: modalFooter, modalClasses: "max-w-4xl w-full", extra_func: extra });
}

function onBenchmarkWorkflowTestSuccess(response) {
    markAllStagesAsComplete();
    if (response && response.status === "success") {
        var results = JSON.stringify(response.results || {}, null, 2);
        showNextModal(results);
    } else {
        showErrorModal("Benchmark Workflow Test Failed", response);
    }
}

function benchmarkWorkflowTest(workflowTestButton) {
    addSpinner(workflowTestButton);
    var form = document.getElementById("workflow-test-form");
    var formData = form ? new FormData(form) : new FormData();
    disableElements("#workflow-test-form input, #workflow-test-form button");
    ajaxRequest("/benchmarks/run_workflow_test", "POST", formData, onBenchmarkWorkflowTestSuccess, "Error running benchmark workflow test:");
    showPanel("Running Workflow Test...");
    getTaskId().then(function (id) { window.runningTaskId = id; });
    if (typeof streamEvents === "function") streamEvents(logPanel, stagesList, currentStageElement);
}

function showRegisterBenchmark() {
    window.location.href = "/benchmarks/register/ui";
}

function checkWorkflowTestFormValidity() {
    var dataPrepEl = document.getElementById("data-preparation");
    var modelPathEl = document.getElementById("model-path");
    var evaluatorPathEl = document.getElementById("evaluator-path");
    var dataPathEl = document.getElementById("data-path");
    var labelsPathEl = document.getElementById("labels-path");
    var dataPrepPath = dataPrepEl ? dataPrepEl.value.trim() : "";
    var modelPath = modelPathEl ? modelPathEl.value.trim() : "";
    var evaluatorPath = evaluatorPathEl ? evaluatorPathEl.value.trim() : "";
    var dataPath = dataPathEl ? dataPathEl.value.trim() : "";
    var labelsPath = labelsPathEl ? labelsPathEl.value.trim() : "";
    var isValid = dataPrepPath.length > 0 && dataPrepPath.endsWith(".yaml") && modelPath.length > 0 && modelPath.endsWith(".yaml") && evaluatorPath.length > 0 && evaluatorPath.endsWith(".yaml") && dataPath.length > 0 && labelsPath.length > 0;
    var runBtn = document.getElementById("run-workflow-test-btn");
    if (runBtn) runBtn.disabled = !isValid;
}

function init() {
    var runBtn = document.getElementById("run-workflow-test-btn");
    if (runBtn) runBtn.addEventListener("click", function (e) { showConfirmModal(e.currentTarget, benchmarkWorkflowTest, "run the workflow test?"); });
    var form = document.getElementById("workflow-test-form");
    if (form) form.querySelectorAll("input").forEach(function (el) { el.addEventListener("keyup", checkWorkflowTestFormValidity); });
    function addBrowse(id, handler, withFiles) {
        var btn = document.getElementById(id);
        if (btn) btn.addEventListener("click", function () { browseWithFiles = !!withFiles; browseFolderHandler(handler); });
    }
    addBrowse("browse-data-prep-btn", "data-preparation", true);
    addBrowse("browse-data-prep-parameters-btn", "data-preparation-parameters", true);
    addBrowse("browse-data-prep-additional-btn", "data-preparation-additional", false);
    addBrowse("browse-ref-btn", "model-path", true);
    addBrowse("browse-ref-parameters-btn", "model-parameters-path", true);
    addBrowse("browse-ref-additional-btn", "model-additional-path", false);
    addBrowse("browse-metrics-btn", "evaluator-path", true);
    addBrowse("browse-metrics-parameters-btn", "evaluator-parameters-path", true);
    addBrowse("browse-metrics-additional-btn", "evaluator-additional-path", false);
    addBrowse("browse-data-btn", "data-path", false);
    addBrowse("browse-labels-btn", "labels-path", false);
    checkWorkflowTestFormValidity();
}
if (document.readyState === "loading") document.addEventListener("DOMContentLoaded", init);
else init();
